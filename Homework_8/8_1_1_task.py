# Упражнение 1: Операции со строкой

text = "Python Programming"

# 1. Длина строки
print("Длина строки:", len(text)) 

# 2. Символ по индексу 7
print("Символ с индексом 7:", text[7]) 

# 3. Последние 3 символа
print("Последние 3 символа:", text[-3:]) 

# 4. Проверка наличия подстроки "gram"
print("Содержится ли 'gram' в строке?:", text.find("gram") != -1) 
